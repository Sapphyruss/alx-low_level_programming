int add(int a, int b)
{
	return (a + b);
}

/**
 * sub - subtract two number
 * @a: first number
 * @b: second number
 * Return: the sub of two numbers
 */
int sub(int a, int b)
{
	return (a - b);
}

/**
 * div - divide two numbers
 * @a: first number
 * @b: second number
 * Return: the divide of two number
 */
int div(int a, int b)
{
	return (a / b);
}

/**
 * mul - multiply a by b
 * @a: first number
 * @b: second number
 * Return: multiple a by b.
 */
int mul(int a, int b)
{
	return (a * b);
}

/**
 * mod - reminder of a by b
 * @a: first number
 * @b: second number
 * Return: reminder of a by b
 */
int mod(int a, int b)
{
	return (a % b);
}
